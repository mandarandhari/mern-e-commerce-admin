const mongoose = require('mongoose');
const mongoosePaginate = require('mongoose-paginate-v2');

const CustomerSchema = new mongoose.Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    created_at: {
        type: Date,
        default: Date.now()
    },
    updated_at: {
        type: Date,
        default: null
    }
});

CustomerSchema.plugin(mongoosePaginate);

module.exports = mongoose.model('customers', CustomerSchema);